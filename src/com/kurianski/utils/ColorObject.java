package com.kurianski.utils;

import java.util.ArrayList;

public class ColorObject {
	public ArrayList<Integer> red;
	public ArrayList<Integer> green;
	public ArrayList<Integer> blue;
	
	public ColorObject(ArrayList<Integer> red, ArrayList<Integer> green, ArrayList<Integer> blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
}
